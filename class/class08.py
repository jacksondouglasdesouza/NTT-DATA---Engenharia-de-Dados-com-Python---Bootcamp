# Tipos de operadores em Python

# Operadores de comparação

# Igualdade ==
# Diferença !=
# Maior >
# Menor <
# Maior ou igual >=
# Menor ou igual <=

# Exemplos:

print(2 == 2)
print(2 == 3)
print(2 != 3)
print(2 != 2)
print(2 > 3)
print(2 < 3)
print(2 >= 3)
print(2 <= 3)


saldo = 450
saque = 200

print(f'Valores de comparrações {saldo} == {saque}')
print(saldo == saque)
print(f'Valores de comparrações {saldo} != {saque}')
print(saldo != saque)
print(f'Valores de comparrações {saldo} > {saque}')
print(saldo > saque)
print(f'Valores de comparrações {saldo} < {saque}')
print(saldo < saque)
print(f'Valores de comparrações {saldo} >= {saque}')
print(saldo >= saque)
print(f'Valores de comparrações {saldo} <= {saque}')
print(saldo <= saque)
